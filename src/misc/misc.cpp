string IntToString(int n)
{
	char buff[64];	// ‘½‚ß‚É
	char *bp=buff;
	string ret;

	if(n<0) {
		ret+='-';
		n*=-1;
	}

	do {
		*(bp++)=(n%10)+'0';
		n/=10;
	} while(n>0);

	while(bp!=buff) {
		bp--;
		ret+=*bp;
	}

	return(ret);
}
